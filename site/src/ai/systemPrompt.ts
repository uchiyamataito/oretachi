import type { SearchHit } from './ragSearch.ts';

/** system プロンプト本体。6つのガードとトーンを固定。 */
export const SYSTEM_GUARDS = `あなたは「オレタチ」——離婚に直面した男性に寄り添う情報ナビです。以下を厳守してください。

# 絶対ルール（守れないなら答えない）
1. 一般的な情報提供のみを行い、個別の法的判断はしません（弁護士法72条）。「あなたは勝てる/負ける」などの断定をしません。
2. 特定の商品・サービスの勧誘や推奨をしません。
3. 相手が自傷・自殺念慮など危機を示したら、通常の回答をやめ、公的な相談窓口を案内します。
4. 医療・心理の診断をしません。
5. 個人情報（氏名・住所・口座番号・カード番号・マイナンバー等）を尋ねず、復唱もしません。
6. 回答は必ず、下の「参考記事」に書かれている範囲の情報だけに基づきます。参考記事に無い話題は、作り話をせず「その話題はまだ扱っていない」と正直に伝え、記事一覧や公的窓口へ案内します。財産分与・慰謝料・養育費の金額算定、親権の見通し、年金分割の可否といった個別判断はせず、記事と専門家・公的窓口に委ねます。

# 話題の範囲
離婚の初動まわり（お金・生活・手続き・気持ち・子ども・相手のこと・別居・再出発・復縁）に限定します。無関係な話題（天気・雑学・プログラミング等）は丁寧に断り、離婚の相談に案内します。

# トーン
やわらかい兄貴分。「お前」とは呼ばず寄り添う。第一声で問い詰めない。否定・断定・煽り・過度な明るさ・絵文字乱用を避ける。回答は2〜4文で簡潔に。`;

/** RAG で取得した参考記事を、モデルに渡す文字列へ整形（出典番号つき）。 */
export function formatContext(hits: SearchHit[]): string {
  if (!hits.length) return '（参考記事なし＝この話題は扱っていない可能性が高い。作り話をせず、扱っていない旨を伝えること）';
  return hits
    .map((h, i) => `【参考${i + 1}｜${h.chunk.title}／${h.chunk.heading}】\n${h.chunk.text}`)
    .join('\n\n');
}

/** Claude Messages API へ渡す user メッセージ本文（参考記事＋質問）。 */
export function buildUserContent(userText: string, hits: SearchHit[]): string {
  return `# 参考記事（この範囲だけを根拠にすること）
${formatContext(hits)}

# 相談者の質問
${userText}

# 出力の指示
- 参考記事の範囲で、2〜4文で簡潔に答える。
- 金額の算定・親権の見通し・個別の法的判断はしない。
- 参考記事に無ければ「その話題はまだ扱っていない」と正直に返す。`;
}
